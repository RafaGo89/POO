public class Extra06{
    public static void main(String[] agrs){
        for(int i = 1; i <= 5; i++){  // ciclo que maneja las filas
            for(int j = 1; j <= 5; j++){  // ciclo que imprime los ateriscos
                System.out.print("* ");
            } //FOR
            System.out.println(); // salto de línea
        } // FOR
    } // MAIN
} // CLASE PUBLICA