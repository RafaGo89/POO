public class Ejercicio2{

	public static void main(String[] args){
		// Variables flotante
		float peso = 40.6f;		

		System.out.println("Tipo de dato flotante con f: " + peso);

		// Variable caracter
		char inicial = 'r';		

		System.out.println("Mi inicial es " + inicial);

		// Constante
		final double PI = 3.1416;
		
		System.out.println("Pi es igual " + PI);

		// Arreglo
		int[] edades = new int [5];
	}
}